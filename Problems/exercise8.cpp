#include <iostream>
using namespace std;

int main() {
    // Ejercicio 8: Determinar la categoría según la edad
    int edad=0;
    
    cout<<"Cual es tu edad";
    cin>>edad;

    if(edad<12){
        cout<<"Eres un niño";
    }else if(edad>=12&&edad<=17){
        cout<<"Eres un adolescente";
    }else if(edad>=18&&edad<=64){
        cout<<"Eres un adulto";
    }else if(edad>65){
        cout<<"Eres un adulto mayor";
    }

    return 0;
}

