#include <iostream>
using namespace std;

int main() {
    // Ejercicio 6: Determinar si un número es positivo, negativo o cero
    int num;
    cout<<"Ingrese el numero: "<<endl;
    cin>>num;

    if(num>0){
        cout<<"Este numero es positivo";
    }else if(num<0){
        cout<<"Este numero es negativo";
    }else{
        cout<<"Este numero es cero";
    }

    return 0;
}
